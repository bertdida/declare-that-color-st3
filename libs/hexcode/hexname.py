import re
from . import hexutils

MATCH_NAME_RE = r'(?i)^(?:{0}-[0-9]+|{0})$'

INDEX_ERROR_TEMPLATE = (
    "DeclareThatColor: color name not found: {}"
)


def get(hex_code):

    hex_code = hexutils.normalize(hex_code)

    r, g, b = hexutils.rgb(hex_code)
    h, s, l = hexutils.hsl(hex_code)

    ndf1 = 0
    ndf2 = 0
    ndf = 0
    cl = -1
    df = -1

    for i, color_name in enumerate(color_names):

        if hex_code == color_name[0]:
            return color_name[1]

        ndf1 = (
            pow(r - color_name[2], 2) +
            pow(g - color_name[3], 2) +
            pow(b - color_name[4], 2)
        )

        ndf2 = (
            pow(h - color_name[5], 2) +
            pow(s - color_name[6], 2) +
            pow(l - color_name[7], 2)
        )

        ndf = ndf1 + ndf2 * 2

        if df < 0 or df > ndf:
            df = ndf
            cl = i

    if cl < 0:
        raise ValueError(VALUE_ERROR_TEMPLATE.format(hex_code))

    try:
        return color_names[cl][1]
    except IndexError:
        raise ValueError(INDEX_ERROR_TEMPLATE.format(hex_code))


def get_unique(hex_code, names: list):

    curr_name = get(hex_code)

    if curr_name not in names:
        return curr_name

    _is_match = is_match(curr_name)
    total_match = 1

    for name in names:
        if _is_match(name):
            total_match += 1

    return '{}-{}'.format(curr_name, total_match)


def is_match(base_name):

    def result(name):

        name = remove_suffix(name)
        match = re.match(MATCH_NAME_RE.format(name), base_name)

        return bool(match)

    return result


def remove_suffix(name):

    *rest, suffix = name.split('-')

    if not suffix.isdigit():
        return name

    return '-'.join(rest)


